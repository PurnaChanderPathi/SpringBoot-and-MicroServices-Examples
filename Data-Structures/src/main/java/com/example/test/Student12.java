package com.example.test;

public class Student12 {

	private String name;
    private int maths;
    private int science;
    private int english;

    public Student12(String name, int maths, int science, int english) {
        this.name = name;
        this.maths = maths;
        this.science = science;
        this.english = english;
    }

    public String getName() {
        return name;
    }

    public int getMaths() {
        return maths;
    }

    public int getScience() {
        return science;
    }

    public int getEnglish() {
        return english;
    }
}
